def build_faq_schema(tx):
    items = []
    for i in range(1, 6):
        q = tx[f"faq_{i}_q"]
        a = tx[f"faq_{i}_a"]
        items.append(f"""        {{
          "@type": "Question",
          "name": "{q}",
          "acceptedAnswer": {{
            "@type": "Answer",
            "text": "{a}"
          }}
        }}""")
    return ",\n".join(items)

