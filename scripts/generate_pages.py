    page = template
    for key, value in tx.items():
        page = page.replace(f"%%{key.upper()}%%", value)
    # Replace any remaining %% markers
    for key in tx:
        page = page.replace(f"%%{key}%%", tx[key])
    # Handle FAQ schema
    faq_schema = build_faq_schema(tx)
    page = page.replace("%%FAQ_SCHEMA%%", faq_schema)

